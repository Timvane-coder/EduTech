// Example usage - Complete Nucleic Acids Workbook with Diagrams and DOCX Export
import { EnhancedMolecularBiologyWorkbook } from './molecularbiology.js';
import { Document, Packer, Paragraph, TextRun, HeadingLevel, Table, TableRow, TableCell, ImageRun, AlignmentType, WidthType, BorderStyle } from 'docx';
import fs from 'fs';
import path from 'path';

// ========================================
// DOCX EXPORTER CLASS
// ========================================
class MolecularBiologyDocxExporter {
    constructor(workbookResult) {
        this.result = workbookResult;
        this.content = workbookResult.content;
        this.workbook = workbookResult.workbook;
        this.diagrams = workbookResult.diagrams;
    }

    async exportToDocx(outputPath) {
        console.log(`   📝 Creating DOCX document...`);
        const sections = [];

        // Title Page
        sections.push(...this.createTitlePage());

        // Table of Contents placeholder
        sections.push(
            new Paragraph({
                text: 'Table of Contents',
                heading: HeadingLevel.HEADING_1,
                spacing: { before: 400, after: 200 }
            }),
            new Paragraph({
                text: '(Contents will be generated by Word)',
                spacing: { after: 400 }
            })
        );

        // Process each workbook section
        console.log(`   📚 Processing ${this.workbook.sections.length} sections...`);
        for (const section of this.workbook.sections) {
            sections.push(...await this.createSection(section));
        }

        // Add diagrams section with embedded images
        if (this.diagrams?.renderedImages?.length > 0) {
            console.log(`   🎨 Adding ${this.diagrams.renderedImages.length} diagrams...`);
            sections.push(...await this.createDiagramsSection());
        }

        // Create document
        console.log(`   💾 Writing DOCX file...`);
        const doc = new Document({
            sections: [{
                properties: {
                    page: {
                        margin: {
                            top: 1440,
                            right: 1440,
                            bottom: 1440,
                            left: 1440
                        }
                    }
                },
                children: sections
            }]
        });

        // Write to file
        const buffer = await Packer.toBuffer(doc);
        fs.writeFileSync(outputPath, buffer);
        
        console.log(`   ✅ DOCX exported to ${outputPath}`);
        return outputPath;
    }

    createTitlePage() {
        return [
            new Paragraph({
                text: this.workbook.title || 'Molecular Biology Workbook',
                heading: HeadingLevel.TITLE,
                alignment: AlignmentType.CENTER,
                spacing: { before: 2000, after: 400 }
            }),
            new Paragraph({
                text: this.content.topic || 'Molecular Biology',
                heading: HeadingLevel.HEADING_1,
                alignment: AlignmentType.CENTER,
                spacing: { after: 200 }
            }),
            new Paragraph({
                text: `Category: ${this.content.category || 'N/A'}`,
                alignment: AlignmentType.CENTER,
                spacing: { after: 100 }
            }),
            new Paragraph({
                text: `Generated: ${new Date().toLocaleDateString()}`,
                alignment: AlignmentType.CENTER,
                spacing: { after: 400 }
            }),
            new Paragraph({
                text: '',
                pageBreakBefore: true
            })
        ];
    }

    async createSection(section) {
        const paragraphs = [];

        // Section title
        paragraphs.push(
            new Paragraph({
                text: section.title,
                heading: HeadingLevel.HEADING_1,
                spacing: { before: 400, after: 200 }
            })
        );

        // Handle different section types
        switch (section.type) {
            case 'problem':
            case 'overview':
            case 'detailed_content':
            case 'comparisons':
            case 'examples_applications':
            case 'contextual':
            case 'experiments':
            case 'misconceptions':
            case 'metacognitive':
            case 'assessment':
            case 'related_topics':
                if (section.data && Array.isArray(section.data)) {
                    paragraphs.push(...this.createContentFromData(section.data));
                }
                break;

            case 'diagrams':
                paragraphs.push(...await this.createDiagramsSectionContent(section));
                break;

            default:
                paragraphs.push(
                    new Paragraph({
                        text: 'Section content...',
                        spacing: { after: 200 }
                    })
                );
        }

        // Add relevant diagrams after this section
        if (this.shouldAddDiagramsAfterSection(section)) {
            paragraphs.push(...await this.addRelevantDiagrams(section));
        }

        return paragraphs;
    }

    createContentFromData(data) {
        const paragraphs = [];

        for (const row of data) {
            if (!row || row.length === 0) {
                paragraphs.push(new Paragraph({ text: '', spacing: { after: 100 } }));
            } else if (row[0] && row[0].toUpperCase() === row[0] && row[0].length > 0 && (!row[1] || row[1] === '')) {
                // Header
                paragraphs.push(
                    new Paragraph({
                        text: row[0],
                        heading: HeadingLevel.HEADING_2,
                        spacing: { before: 200, after: 100 }
                    })
                );
            } else if (row[0] && row[0].startsWith('  ')) {
                // Bullet point
                paragraphs.push(
                    new Paragraph({
                        text: row.join(' ').trim(),
                        bullet: { level: 0 },
                        spacing: { after: 100 }
                    })
                );
            } else {
                // Regular paragraph
                paragraphs.push(
                    new Paragraph({
                        children: [
                            new TextRun({ text: String(row[0] || ''), bold: true }),
                            new TextRun({ text: ': ' }),
                            new TextRun({ text: String(row[1] || '') })
                        ],
                        spacing: { after: 100 }
                    })
                );
            }
        }

        return paragraphs;
    }

    shouldAddDiagramsAfterSection(section) {
        const relevantSections = ['overview', 'detailed_content', 'comparisons'];
        return relevantSections.includes(section.type);
    }

    async addRelevantDiagrams(section) {
        const paragraphs = [];
        
        if (!this.diagrams?.renderedImages || this.diagrams.renderedImages.length === 0) {
            return paragraphs;
        }

        const relevantDiagrams = this.diagrams.renderedImages.slice(0, 2);

        if (relevantDiagrams.length === 0) {
            return paragraphs;
        }

        paragraphs.push(
            new Paragraph({
                text: 'Related Diagrams',
                heading: HeadingLevel.HEADING_2,
                spacing: { before: 300, after: 200 }
            })
        );

        for (const diagram of relevantDiagrams) {
            if (diagram.type === 'image') {
                paragraphs.push(...await this.createDiagramParagraph(diagram));
            }
        }

        return paragraphs;
    }

    async createDiagramParagraph(diagram) {
        const paragraphs = [];

        try {
            if (diagram.type === 'image' && diagram.buffer) {
                paragraphs.push(
                    new Paragraph({
                        text: this.formatDiagramTitle(diagram.key),
                        heading: HeadingLevel.HEADING_3,
                        spacing: { before: 200, after: 100 }
                    })
                );

                const maxWidth = 6 * 914400;
                const aspectRatio = diagram.height / diagram.width;
                const width = maxWidth;
                const height = Math.round(width * aspectRatio);

                paragraphs.push(
                    new Paragraph({
                        children: [
                            new ImageRun({
                                data: diagram.buffer,
                                transformation: {
                                    width: width,
                                    height: height
                                }
                            })
                        ],
                        alignment: AlignmentType.CENTER,
                        spacing: { after: 200 }
                    })
                );

                paragraphs.push(
                    new Paragraph({
                        text: `Figure: ${this.formatDiagramTitle(diagram.key)}`,
                        alignment: AlignmentType.CENTER,
                        italics: true,
                        spacing: { after: 300 }
                    })
                );
            }
        } catch (error) {
            console.error(`   ⚠️  Error adding diagram ${diagram.key}:`, error.message);
        }

        return paragraphs;
    }

    async createDiagramsSection() {
        const paragraphs = [];

        paragraphs.push(
            new Paragraph({
                text: 'Molecular Diagrams',
                heading: HeadingLevel.HEADING_1,
                spacing: { before: 400, after: 200 },
                pageBreakBefore: true
            })
        );

        for (const diagram of this.diagrams.renderedImages) {
            if (diagram.type === 'image') {
                paragraphs.push(...await this.createDiagramParagraph(diagram));
            }
        }

        return paragraphs;
    }

    async createDiagramsSectionContent(section) {
        const paragraphs = [];

        if (section.diagrams) {
            for (const diagram of section.diagrams) {
                if (diagram.type === 'image') {
                    paragraphs.push(...await this.createDiagramParagraph(diagram));
                }
            }
        }

        return paragraphs;
    }

    formatDiagramTitle(key) {
        return key
            .replace(/([A-Z])/g, ' $1')
            .replace(/_/g, ' ')
            .trim()
            .split(' ')
            .map(word => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
            .join(' ');
    }
}

// ========================================
// HELPER FUNCTIONS
// ========================================
async function exportDiagrams(result, outputDir) {
    if (!result.diagrams?.renderedImages) {
        console.log('   ⚠️  No diagrams to export');
        return;
    }
    
    const diagramDir = path.join(outputDir, 'diagrams');
    if (!fs.existsSync(diagramDir)) {
        fs.mkdirSync(diagramDir, { recursive: true });
    }
    
    let exportedCount = 0;
    for (const diagram of result.diagrams.renderedImages) {
        if (diagram.type === 'image' && diagram.buffer) {
            const filepath = path.join(diagramDir, `${diagram.key}.png`);
            fs.writeFileSync(filepath, diagram.buffer);
            exportedCount++;
        }
    }
    
    console.log(`   💾 Exported ${exportedCount} PNG diagrams to ${diagramDir}`);
}

async function exportWithDocx(result, outputDir) {
    console.log(`\n   📦 Exporting to ${outputDir}...`);
    
    if (!fs.existsSync(outputDir)) {
        fs.mkdirSync(outputDir, { recursive: true });
    }
    
    // Export diagrams as PNG files
    await exportDiagrams(result, outputDir);
    
    // Export to DOCX with embedded diagrams
    const docxPath = path.join(outputDir, 'workbook.docx');
    const exporter = new MolecularBiologyDocxExporter(result);
    await exporter.exportToDocx(docxPath);
    
    // Export content JSON
    const contentPath = path.join(outputDir, 'content.json');
    fs.writeFileSync(contentPath, JSON.stringify(result.content, null, 2));
    console.log(`   💾 Content JSON saved`);
    
    // Create summary
    const summary = {
        generated: new Date().toISOString(),
        topic: result.content.topic,
        category: result.content.category,
        diagramCount: result.diagrams?.renderedImages?.length || 0,
        diagrams: result.diagrams?.renderedImages?.map(d => d.key) || [],
        exports: {
            docx: 'workbook.docx',
            content: 'content.json',
            diagrams: 'diagrams/'
        }
    };
    
    const summaryPath = path.join(outputDir, 'summary.json');
    fs.writeFileSync(summaryPath, JSON.stringify(summary, null, 2));
    console.log(`   💾 Summary JSON saved`);
    
    console.log(`   ✅ Export complete!\n`);
}

