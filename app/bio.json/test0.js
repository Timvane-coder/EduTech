// EXPORT WITH EXPERIMENT FUNCTION
// ========================================
async function exportWithExperiment(result, outputDir) {
    console.log(`\n   📦 Exporting to ${outputDir}...`);

    if (!fs.existsSync(outputDir)) {
        fs.mkdirSync(outputDir, { recursive: true });
    }

    // Export diagrams as PNG files
    await exportDiagrams(result, outputDir);

    // Export to DOCX with embedded diagrams AND experiment
    const docxPath = path.join(outputDir, 'workbook.docx');
    const exporter = new MolecularBiologyDocxExporterWithExperiment(result);
    await exporter.exportToDocx(docxPath);

    // Export content JSON
    const contentPath = path.join(outputDir, 'content.json');
    fs.writeFileSync(contentPath, JSON.stringify(result.content, null, 2));
    console.log(`   💾 Content JSON saved`);

    // Export experiment JSON
    if (result.experiment) {
        const experimentPath = path.join(outputDir, 'experiment.json');
        fs.writeFileSync(experimentPath, JSON.stringify(result.experiment, null, 2));
        console.log(`   💾 Experiment JSON saved`);
    }

    // Create summary
    const summary = {
        generated: new Date().toISOString(),
        topic: result.content.topic,
        category: result.content.category,
        diagramCount: result.diagrams?.renderedImages?.length || 0,
        hasExperiment: !!result.experiment,
        experimentName: result.experiment?.name || 'N/A',
        diagrams: result.diagrams?.renderedImages?.map(d => d.key) || [],
        exports: {
            docx: 'workbook.docx',
            content: 'content.json',
            experiment: 'experiment.json',
            diagrams: 'diagrams/'
        }
    };

    const summaryPath = path.join(outputDir, 'summary.json');
    fs.writeFileSync(summaryPath, JSON.stringify(summary, null, 2));
    console.log(`   💾 Summary JSON saved`);

    console.log(`   ✅ Export complete!\n`);
}

